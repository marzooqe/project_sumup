
--Average transacted amount per store typology and country

SELECT
    COUNTRY,
    TYPOLOGY,
    ROUND(SUM(AMOUNT) / COUNT(DISTINCT ID), 2) AS AVG_TRANSACTED_AMOUNT
FROM SUMUP."REPORTING".TRANSACTION_PERFORMANCE
GROUP BY
    COUNTRY,
    TYPOLOGY
ORDER BY
    COUNTRY ASC,
    AVG_TRANSACTED_AMOUNT DESC